// ---- Dist artifact existence ----

describe("dist artifacts (post-build)", () => {
  const DIST = resolve(REPO_ROOT, "dist");

  const requiredArtifacts = [
    "dist/src/index.js",
    "dist/bin/setup.js",
    "dist/hooks/user-prompt-submit.js",
    "dist/hooks/post-tool-use.js",
    "dist/hooks/pre-compact.js",
    "dist/hooks/git-attribution.js",
    "dist/hooks/status.js",
  ];

  for (const artifact of requiredArtifacts) {
    it(`${artifact} exists`, () => {
      expect(existsSync(resolve(REPO_ROOT, artifact))).toBe(true);
    });
  }

  it("no source .ts files shipped in dist (.d.ts declarations are fine)", () => {
    // Verify we don't accidentally ship source files. `.d.ts` declaration
    // files are legitimate tsc output, so only flag non-declaration .ts files.
    const { execSync } = require("child_process");
    const tsFiles = execSync(
      `find ${DIST} -name "*.ts" -not -name "*.d.ts" 2>/dev/null || true`
    )
      .toString()
      .trim();
    expect(tsFiles).toBe("");
  });
});
